# Array functions

function array_defined
{
  [ -n "`declare -p $1 2> $NULL | grep \"declare -a\"`" ]
}

function array_contains
{
  eval ARRAY="(\"\${$1[@]}\")"

  for (( I=0; I < ${#ARRAY[*]}; I++ )); do
    [ "${ARRAY[$I]}" == "$2" ] && return 0
  done

  return 1
}

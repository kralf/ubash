# Math functions

function math_calc
{
  EXPR="scale=0; ($1)/1"
  define $2 `echo $EXPR | bc`
}

function math_inc
{
  math_calc "${!1}+1" $1
}

function math_dec
{
  math_calc "${!1}-1" $1
}
